public class Coordinates {

    private int x;
    private int y;
    private boolean On;

    public boolean getOn() {
        return On;
    }

    public void setOn(boolean on) {
        On = on;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
